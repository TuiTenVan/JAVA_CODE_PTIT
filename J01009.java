import java.util.Scanner;

public class J01009 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();
        long sum = 0;
        long gt = 1;
        for(int i = 1; i <= n; i++){
            gt *= i;
            sum += gt;
        }
        System.out.println(sum);
    }
}
